class SignIn{
  String _facebookID;
  String _gmail;
  String _password;
  String _email;

  String get facebookID => _facebookID;

  set facebookID(String value) {
    _facebookID = value;
  }

  String get gmail => _gmail;

  String get email => _email;

  set email(String value) {
    _email = value;
  }

  String get password => _password;

  set password(String value) {
    _password = value;
  }

  set gmail(String value) {
    _gmail = value;
  }

}